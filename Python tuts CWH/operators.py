# operators in python
#arithmatic operators
print(15+6) #addition
print(15-6) #subtraction
print(15*6) #multiplication
print(15/6) #division
print(15//6) #floor- used for round up value in integer(first number of calculation), ignore the after point value
print(5**6) #exponential(5 raise to the power 6)
print(9%5) #modulus- gives reminder

#assignment operators
h=5
i+=1 #i=i+1
print(h)
print(i)

#comparision operator

k=1
print(k==0)
print(k<=0)
print(k>=0)
print(k!=0)

# logical operator
a=True
b=False
c=False
print(a and b)
print(a or b)
print(not c)

# identity operators
print(a is b)
print(b is c)
print(a is not b)

